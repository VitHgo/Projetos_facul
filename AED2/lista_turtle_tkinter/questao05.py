import turtle as t

t.up()
t.goto(-300,300)
t.down()
t.fillcolor('red')

t.begin_fill()
t.rt(60)
t.fd(150)
t.lt(120)
t.fd(150)
t.lt(120)
t.fd(150)
t.rt(180)
t.end_fill()
t.up()

t.goto(-120,300)
t.down()
t.fillcolor('green')

t.begin_fill()
t.rt(60)
t.fd(150)
t.rt(120)
t.fd(150)
t.rt(120)
t.fd(150)
t.end_fill()
t.up()

t.goto(-90,300)
t.down()
t.fillcolor('blue')

t.begin_fill()
t.rt(60)
t.fd(150)
t.rt(120)
t.fd(150)
t.rt(120)
t.fd(150)
t.end_fill()

t.hideturtle()
t.mainloop()